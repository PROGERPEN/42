int		ft_strcmp(char *s1, char *s2)
{
	int firstlen;
	int secondlen;

	while (*s1 && *s2)
	{
		if (*s1 == *s2)
		{
			s1++;
			s2++;
		}
		else
			return (*s1 - *s2);
	}
	return (0);
}
